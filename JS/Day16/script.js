/*Area of Triangle*/

var base=25
var height=10

var areaTriangle=1/2*base*height
console.log("Area of Triangle: "+areaTriangle );


/*Area of Cricle*/

var radius=15
var pi=3.14

var areaCircle=pi*radius*radius
console.log("Area of Circle: "+areaCircle );

/*Area of Rectangle*/

var lenght=25
var width=12
var areaRectangle=lenght*width

console.log("Area of Rectangle: "+areaRectangle );


